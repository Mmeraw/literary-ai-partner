import {
  runRevisionSurfaceOwnershipGate,
  checkRenderedOutputForbiddenHeadings,
  runRenderedOutputOwnershipGate,
  buildRevisionSurfaceOwnershipDiagnosis,
  FORBIDDEN_RENDERED_HEADINGS,
  type RevisionSurfaceOwnershipGateResult,
} from '@/lib/evaluation/revisionSurfaceOwnershipGate';
import type { UnifiedEvaluationDocument } from '@/lib/evaluation/unifiedEvaluationDocument';

function makeMinimalUED(overrides: Partial<UnifiedEvaluationDocument> = {}): UnifiedEvaluationDocument {
  return {
    templateMode: 'short_form_evaluation',
    title: 'Test Manuscript',
    titleBlock: {
      reportType: 'Short-Form Evaluation',
      genre: 'Literary Fiction',
      targetAudience: 'General readership',
      overallScoreLabel: '7/10',
      overallScoreConfidenceLabel: 'High Confidence',
      marketReadiness: 'Near Market Ready',
      marketReadinessConfidenceLabel: 'High Confidence',
      shelf: null,
      shelfConfidenceLabel: null,
    },
    oneParagraphPitch: 'A compelling story.',
    oneSentencePitch: 'A story about life.',
    premise: 'The premise of this novel...',
    contentWarnings: [],
    revisionOpportunitySummary: {
      total: 3,
      high: 1,
      medium: 1,
      low: 1,
    },
    executiveSummary: 'This manuscript shows promise.',
    topStrengths: ['Strong voice', 'Compelling characters'],
    topRisks: ['Pacing issues in act 2'],
    topRecommendations: ['Tighten middle act pacing', 'Develop antagonist arc'],
    criteriaScoreGrid: [],
    criterionDetails: [
      {
        label: 'Plot',
        scoreLabel: '7/10',
        confidenceLabel: 'High Confidence',
        supportLabel: 'Strong',
        rationaleLabel: 'Rationale',
        rationaleText: 'The plot has strong bones but pacing falters.',
        recommendations: [
          { opportunity_id: 'opp-001', action: 'Fix pacing in chapter 12' },
          { opportunity_id: 'opp-002', action: 'Add tension to climax' },
        ],
      },
      {
        label: 'Character',
        scoreLabel: '8/10',
        confidenceLabel: 'High Confidence',
        supportLabel: 'Strong',
        rationaleLabel: 'Rationale',
        rationaleText: 'Characters are well-drawn.',
        recommendations: [
          { opportunity_id: 'opp-003', action: 'Deepen antagonist motivation' },
        ],
      },
    ],
    actionItems: {
      quickWins: [],
      strategicRevisions: [],
    },
    confidenceExplanation: 'Based on full manuscript review.',
    disclaimer: 'Generated by RevisionGrade.',
    canonicalOpportunityLedger: undefined,
    modeSpecific: {
      manuscriptScaleContinuityFindings: [],
      revisionPriorityPlan: [],
      storyLedgerArchitectureMap: [],
      reviewGateReadinessSurface: [],
      governedLedgerAddenda: [],
      crossLayerSynthesis: [],
      layerAwareRevisionSequencing: [],
      continuityCoverageProof: [],
      readinessReleasabilityPosture: '',
    },
    ...overrides,
  } as unknown as UnifiedEvaluationDocument;
}

describe('REVISION_SURFACE_OWNERSHIP_GATE', () => {
  describe('runRevisionSurfaceOwnershipGate', () => {
    it('should pass for a well-formed short-form UED', () => {
      const doc = makeMinimalUED();
      const result = runRevisionSurfaceOwnershipGate(doc);
      expect(result.status).toBe('pass');
      expect(result.failures).toHaveLength(0);
    });

    it('should detect missing opportunity_id in criterion recommendations', () => {
      const doc = makeMinimalUED({
        criterionDetails: [
          {
            label: 'Plot',
            scoreLabel: '7/10',
            confidenceLabel: 'High Confidence',
            supportLabel: 'Strong',
            rationaleLabel: 'Rationale',
            rationaleText: 'The plot needs work.',
            recommendations: [
              { action: 'Fix pacing' }, // Missing opportunity_id
              { opportunity_id: 'opp-001', action: 'Add tension' },
            ],
          },
        ] as UnifiedEvaluationDocument['criterionDetails'],
      });

      const result = runRevisionSurfaceOwnershipGate(doc);
      expect(result.status).toBe('fail');
      expect(result.failures.some((f) => f.failure_code === 'MISSING_OPPORTUNITY_ID')).toBe(true);
    });

    it('should detect tier parity mismatch', () => {
      const doc = makeMinimalUED({
        revisionOpportunitySummary: {
          total: 5,
          high: 1,
          medium: 1,
          low: 1,
        },
      } as Partial<UnifiedEvaluationDocument>);

      const result = runRevisionSurfaceOwnershipGate(doc);
      expect(result.status).toBe('fail');
      expect(result.failures.some((f) => f.failure_code === 'TIER_MISMATCH')).toBe(true);
    });

    it('should detect verbatim duplicate Top Recommendations', () => {
      const doc = makeMinimalUED({
        topRecommendations: ['Fix pacing in chapter 12'],
        criterionDetails: [
          {
            label: 'Plot',
            scoreLabel: '7/10',
            confidenceLabel: 'High Confidence',
            supportLabel: 'Strong',
            rationaleLabel: 'Rationale',
            rationaleText: 'The plot needs work.',
            recommendations: [
              { opportunity_id: 'opp-001', action: 'Fix pacing in chapter 12' },
            ],
          },
        ] as UnifiedEvaluationDocument['criterionDetails'],
      });

      const result = runRevisionSurfaceOwnershipGate(doc);
      expect(result.status).toBe('fail');
      expect(result.failures.some((f) => f.failure_code === 'TOP_RECOMMENDATION_VERBATIM_DUPLICATE')).toBe(true);
    });

    it('should detect forbidden heading embedded in criterion rationale', () => {
      const doc = makeMinimalUED({
        criterionDetails: [
          {
            label: 'Plot',
            scoreLabel: '7/10',
            confidenceLabel: 'High Confidence',
            supportLabel: 'Strong',
            rationaleLabel: 'Rationale',
            rationaleText: '## Action Items\nThis should not be here.',
            recommendations: [
              { opportunity_id: 'opp-001', action: 'Fix something' },
            ],
          },
        ] as UnifiedEvaluationDocument['criterionDetails'],
      });

      const result = runRevisionSurfaceOwnershipGate(doc);
      expect(result.status).toBe('fail');
      expect(result.failures.some((f) => f.failure_code === 'FORBIDDEN_HEADING_IN_UED_CRITERION')).toBe(true);
    });

    it('should not run short-form-specific checks for multi-layer mode', () => {
      const doc = makeMinimalUED({
        templateMode: 'long_form_multi_layer_evaluation',
        criterionDetails: [
          {
            label: 'Plot',
            scoreLabel: '7/10',
            confidenceLabel: 'High Confidence',
            supportLabel: 'Strong',
            rationaleLabel: 'Rationale',
            rationaleText: '## Action Items\nAllowed in multi-layer.',
            recommendations: [
              { opportunity_id: 'opp-001', action: 'Fix something' },
            ],
          },
        ] as UnifiedEvaluationDocument['criterionDetails'],
      });

      const result = runRevisionSurfaceOwnershipGate(doc);
      // Forbidden heading check only applies to short_form_evaluation
      expect(result.failures.filter((f) => f.failure_code === 'FORBIDDEN_HEADING_IN_UED_CRITERION')).toHaveLength(0);
    });
  });

  describe('checkRenderedOutputForbiddenHeadings', () => {
    it('should detect forbidden headings in rendered output', () => {
      const output = `
## Title Block
Some content here.

## Action Items
- Fix this
- Fix that

## Top Recommendations
More content.
`;
      const failures = checkRenderedOutputForbiddenHeadings(output, 'webpage');
      expect(failures).toHaveLength(1);
      expect(failures[0].failure_code).toBe('FORBIDDEN_HEADING_IN_RENDERED_OUTPUT');
      expect(failures[0].renderer).toBe('webpage');
      expect(failures[0].section).toBe('Action Items');
    });

    it('should detect multiple forbidden headings', () => {
      const output = `
## Strategic Revisions
Items here.

## Review Gate
Gate content.

## Releasability Assessment
Assessment content.
`;
      const failures = checkRenderedOutputForbiddenHeadings(output, 'pdf');
      expect(failures).toHaveLength(3);
      expect(failures.map((f) => f.section)).toEqual(
        expect.arrayContaining(['Strategic Revisions', 'Review Gate', 'Releasability Assessment']),
      );
    });

    it('should pass clean output', () => {
      const output = `
## Title Block
Manuscript evaluation.

## Executive Summary
A solid manuscript.

## Score Grid
| Criterion | Score |

## Top Recommendations
- Tighten pacing
`;
      const failures = checkRenderedOutputForbiddenHeadings(output, 'txt');
      expect(failures).toHaveLength(0);
    });

    it('should detect all 14 forbidden headings', () => {
      for (const heading of FORBIDDEN_RENDERED_HEADINGS) {
        const output = `## ${heading}\nSome content.`;
        const failures = checkRenderedOutputForbiddenHeadings(output, 'webpage');
        expect(failures.length).toBeGreaterThan(0);
        expect(failures[0].section).toBe(heading);
      }
    });
  });

  describe('runRenderedOutputOwnershipGate', () => {
    it('should validate across all provided surfaces', () => {
      const result = runRenderedOutputOwnershipGate({
        webpage: '## Action Items\nContent.',
        pdf: '## Strategic Revisions\nContent.',
        docx: '## Top Recommendations\nClean.',
        txt: '## Score Grid\nClean.',
      });

      expect(result.status).toBe('fail');
      expect(result.failures).toHaveLength(2);
      expect(result.failures[0].renderer).toBe('webpage');
      expect(result.failures[1].renderer).toBe('pdf');
    });

    it('should pass when all surfaces are clean', () => {
      const result = runRenderedOutputOwnershipGate({
        webpage: '## Executive Summary\nGood.',
        pdf: '## Score Grid\nScores.',
        docx: '## Top Recommendations\nRecs.',
        txt: '## Disclaimer\nEnd.',
      });

      expect(result.status).toBe('pass');
      expect(result.failures).toHaveLength(0);
    });
  });

  describe('buildRevisionSurfaceOwnershipDiagnosis', () => {
    it('should produce a failure_diagnosis_v1-compatible payload', () => {
      const gateResult: RevisionSurfaceOwnershipGateResult = {
        status: 'fail',
        failures: [
          {
            failure_code: 'FORBIDDEN_HEADING_IN_RENDERED_OUTPUT',
            renderer: 'webpage',
            section: 'Action Items',
            expected_behavior: 'Must not contain forbidden heading',
            actual_behavior: 'Found "Action Items" in webpage output',
            remediation_hint: 'Remove the heading.',
          },
        ],
        checked_at: '2026-06-22T21:00:00.000Z',
      };

      const diagnosis = buildRevisionSurfaceOwnershipDiagnosis(gateResult, 'job-123');

      expect(diagnosis.failure_code).toBe('REVISION_SURFACE_OWNERSHIP_GATE_FAILED');
      expect(diagnosis.failure_class).toBe('governance_blocked');
      expect(diagnosis.failure_point.gate).toBe('REVISION_SURFACE_OWNERSHIP_GATE');
      expect(diagnosis.blocking_reasons).toContain('FORBIDDEN_HEADING_IN_RENDERED_OUTPUT');
      expect(diagnosis.revision_surface_ownership_failures).toHaveLength(1);
      expect(diagnosis.admin_summary).toContain('blocked author exposure');
    });
  });
});
