function pushTxtListBlock(lines: string[], label: string, values: string[], options: { ordered?: boolean } = {}): void {
  const items = values.filter(v => v.length > 0);
  if (items.length === 0) return;

  lines.push('');
  lines.push(`${label}:`);
  items.forEach((item, idx) => {
    if (options.ordered) {
      const marker = `${idx + 1}. `;
      pushWrapped(lines, item, { firstIndent: marker, nextIndent: ' '.repeat(marker.length) });
      return;
    }
    pushWrapped(lines, item, { firstIndent: '• ', nextIndent: '  ' });
  });
}

