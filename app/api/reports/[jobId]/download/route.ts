type ExportRecommendation = {
  priority?: 'high' | 'medium' | 'low';
  action?: string;
  expected_impact?: string;
  anchor_snippet?: string;
  symptom?: string;
  mechanism?: string;
  specific_fix?: string;
  reader_effect?: string;
  mistake_proofing?: string;
};

function exportSeverity(priority?: string): string {
  if (priority === 'high') return 'MUST';
  if (priority === 'medium') return 'SHOULD';
  return 'COULD';
}

const SEVERITY_SORT_ORDER: Record<string, number> = { high: 0, medium: 1, low: 2 };

function getCriterionOpportunities(c: { recommendations?: unknown }): ExportRecommendation[] {
  if (!Array.isArray(c.recommendations)) return [];
  return c.recommendations
    .filter((r): r is ExportRecommendation => typeof r === 'object' && r !== null)
    .sort((a, b) => (SEVERITY_SORT_ORDER[a.priority ?? 'low'] ?? 2) - (SEVERITY_SORT_ORDER[b.priority ?? 'low'] ?? 2))
    .slice(0, 3);
}

function opportunityRows(r: ExportRecommendation): Array<[string, string]> {
  const candidates: Array<[string, string | undefined]> = [
    ['Evidence', r.anchor_snippet],
    ['Symptom', r.symptom],
    ['Cause', r.mechanism],
    ['Fix direction', r.specific_fix || r.action],
    ['Reader effect', r.reader_effect || r.expected_impact],
    ['Mistake-proofing', r.mistake_proofing],
  ];

  return candidates.flatMap(([label, value]) => {
    if (typeof value !== 'string' || value.trim().length === 0) return [];
    return [[label, cleanReportText(value)] as [string, string]];
  });
}

function pushTxtListBlock(lines: string[], label: string, values: unknown, options: { ordered?: boolean } = {}): void {
  const cleaned = filterAuthorFacingTextList(values)
    .map((item) => cleanReportText(item, ''))
    .filter((item) => item.length > 0);
  if (cleaned.length === 0) return;

  lines.push(`  ${label}:`);
  cleaned.forEach((item, idx) => {
    if (options.ordered) {
      lines.push(`    ${idx + 1}. ${item}`);
      return;
    }
    lines.push(`    • ${item}`);
  });
}

function isExportableResultCandidate(value: unknown): value is ExportableResult {
  if (isEvaluationResultV1(value) || isEvaluationResultV2(value)) return true;

  if (!value || typeof value !== 'object') return false;

  const candidate = value as ExportableResultShape;
  return (
    typeof candidate.generated_at === 'string' &&
    typeof candidate.overview?.verdict === 'string' &&
    typeof candidate.overview?.one_paragraph_summary === 'string' &&
    Array.isArray(candidate.overview?.top_3_strengths) &&
    Array.isArray(candidate.overview?.top_3_risks) &&
    Array.isArray(candidate.criteria) &&
    Array.isArray(candidate.recommendations?.quick_wins) &&
    Array.isArray(candidate.recommendations?.strategic_revisions)
  );
}

function safeFilename(title: string | null, jobId: string, ext: string): string {
  const base = title ? title.replace(/[^a-z0-9]+/gi, '-').toLowerCase().slice(0, 40) : jobId.slice(0, 8);
  return `revision-grade-${base}.${ext}`;
}

function extractManuscriptTitle(manuscripts: unknown): string | null {
  const relation = Array.isArray(manuscripts) ? manuscripts[0] : manuscripts;
  const title =
    typeof relation === 'object' && relation && 'title' in relation
      ? (relation as { title?: unknown }).title
      : null;
  return typeof title === 'string' && title.trim().length > 0 ? title.trim() : null;
}

function asText(value: unknown): string | null {
  return typeof value === 'string' && value.trim().length > 0 ? value.trim() : null;
}

function scoreLabel(score: number | null | undefined, denominator: number): string {
  return typeof score === 'number' ? `${Math.round(score)}/${denominator}` : `—/${denominator}`;
}

function estimatePages(wordCount: number | null): number | null {
  if (typeof wordCount !== 'number' || !Number.isFinite(wordCount) || wordCount <= 0) return null;
  return Math.max(1, Math.ceil(wordCount / WORDS_PER_MANUSCRIPT_PAGE));
}

function inferReportType(wordCount: number | null): string {
  if (typeof wordCount !== 'number' || !Number.isFinite(wordCount)) return 'Evaluation Report';
  return wordCount >= LONG_FORM_WORD_THRESHOLD ? 'Long-form Evaluation Report' : 'Short-form Evaluation Report';
}

function stripMachineResidue(text: string): string {
  return text
    .replace(/\bindirect_speech\b/g, 'indirect speech')
    .replace(/\breported_speech\b/g, 'reported speech')
    .replace(/\btagged_speech\b/g, 'tagged speech')
    .replace(/\baction_beat_attribution\b/g, 'action-beat attribution')
    .replace(/\bHARD_FAIL\b/g, '')
    .replace(/\bDEGRADED_EXTRACTION\b/g, '')
    .replace(/\bSOURCE_INTEGRITY_REVIEW_REQUIRED\b/g, '')
    .replace(/\bPass\s*[0-9][A-Za-z]?\b/g, '')
    .replace(/\bWAVE\s*(I{1,4}|[1-4])\b/g, '')
    .replace(/\bpipeline\s+(failure|error|status|diagnostic)\b/gi, '')
    .replace(/\bextraction\s+semantics?\b/gi, '')
    .replace(/\bschema\s+validation\b/gi, '')
    .replace(/\bgate[-_]?identifier\b/gi, '')
    .replace(/\bdoctrine[-_]?id\b/gi, '')
    .replace(/\bvalidator[-_]?name\b/gi, '')
    .replace(/\bprompt[-_]?mechanic\w*/gi, '')
    .replace(/\bgovernance[-_]?machinery\b/gi, '')
    .replace(/\s+/g, ' ')
    .trim();
}

function looksTruncated(text: string): boolean {
  const trimmed = text.trim();
  if (!trimmed) return true;
  if (/\blean on extend\.?$/i.test(trimmed)) return true;
  if (/\b(?:and|or|but|while|because|although|through|with|to|from|into|across|between|including|against)$/i.test(trimmed)) return true;
  return trimmed.length > 120 && !/[.!?”’)]$/.test(trimmed);
}

function buildSummaryFallback(result: ExportableResult): string {
  const strengths = result.overview.top_3_strengths.map(stripMachineResidue).filter(Boolean);
  const risks = result.overview.top_3_risks.map(stripMachineResidue).filter(Boolean);
  const strengthSentence = strengths.length
    ? `The strongest areas are: ${strengths.slice(0, 2).join('; ')}.`
    : 'The strongest areas are detailed in the criteria analysis.';
  const riskSentence = risks.length
    ? `The primary risks are: ${risks.slice(0, 2).join('; ')}.`
    : 'The primary risks are detailed in the criteria analysis.';
  return `${strengthSentence} ${riskSentence}`;
}

function safeTruncateText(text: string): string {
  const trimmed = text.trim();
  if (!trimmed) return trimmed;
  if (/[.!?"')\]]\s*$/.test(trimmed)) return trimmed;

  // Find the last complete sentence by locating the last sentence-ending punctuation
  const lastSentenceEnd = Math.max(
    trimmed.lastIndexOf('. '),
    trimmed.lastIndexOf('.' + '\n'),
    trimmed.lastIndexOf('? '),
    trimmed.lastIndexOf('! '),
  );
  // Also check if the text ends with sentence punctuation (possibly followed by close-quote)
  const endsWithSentence = /[.!?]["')\]]*$/.test(trimmed);
  if (endsWithSentence) return trimmed;

  if (lastSentenceEnd > trimmed.length * 0.3) {
    // Cut to the last complete sentence
    return trimmed.slice(0, lastSentenceEnd + 1).trim();
  }

  // Fallback: strip dangling conjunctions and incomplete words
  const DANGLING = /\s+(and|or|but|the|a|an|in|on|at|to|of|for|with|by|than|from|into|as|that|which|who|whose|where|when|while|although|because|before|after|during|between|among|through|about|like|more|less|over|under|also|yet|so|if|whether|not|nor)\s*$/i;
  let result = trimmed;
  while (DANGLING.test(result)) {
    result = result.replace(DANGLING, '');
  }
  // Strip trailing partial words (anything after last space that doesn't look complete)
  const lastSpace = result.lastIndexOf(' ');
  if (lastSpace > 0) {
    const lastWord = result.slice(lastSpace + 1);
    // If the last word ends with a dash, em-dash, or looks like a fragment, strip it
    if (/[—\-]$/.test(lastWord) || (lastWord.length <= 2 && !/^[AI]$/.test(lastWord))) {
      result = result.slice(0, lastSpace).trim();
    }
  }
  result = result.replace(/[,;:\s—\-]+$/, '');
  if (result.length < trimmed.length) return result + '...';
  return result;
}

function cleanReportText(text: unknown, fallback = '—', options: { blockTruncation?: boolean } = {}): string {
  const raw = asText(text);
  if (!raw) return fallback;
  const cleaned = stripMachineResidue(raw);
  if (options.blockTruncation && looksTruncated(cleaned)) {
    return sanitizeCMOS(safeTruncateText(cleaned) || fallback);
  }
  return sanitizeCMOS(cleaned);
}

function toPdfSafeText(value: unknown, fallback = '-'): string {
  return cleanReportText(value, fallback)
    .normalize('NFKC')
    .replace(/[\u0000-\u0008\u000B\u000C\u000E-\u001F\u007F]/g, '')
    .replace(/[\u2018\u2019\u201A\u201B]/g, "'")
    .replace(/[\u201C\u201D\u201E\u201F]/g, '"')
    .replace(/[\u2013\u2014\u2212]/g, '-')
    .replace(/\u2022/g, '-')
    .replace(/\u2122/g, '(TM)')
    .replace(/\u00AE/g, '(R)')
    .replace(/\u00A9/g, '(C)')
    .replace(/[\u00A0\xA0]/g, ' ')
    .replace(/[^\x09\x0A\x0D\x20-\x7E\xA1-\xFF]/g, '?')
    .trim();
}

function buildMetadata(result: ExportableResult, title: string | null, dream: LongformDreamDocument | null): ReportMetadata {
  const wordCount = typeof result.metrics?.manuscript?.word_count === 'number' ? result.metrics.manuscript.word_count : null;
  const displayTitle = title ?? result.metrics?.manuscript?.title ?? 'Untitled Manuscript';
  const shelf = dream?.market_shelf?.best_shelf ?? null;
  const genre = result.metrics?.manuscript?.genre ?? null;
  return {
    displayTitle,
    generatedAt: result.generated_at,
    score: scoreLabel(result.overview.overall_score_0_100, 100),
    verdict: result.overview.verdict.toUpperCase(),
    wordCount,
    estimatedPages: estimatePages(wordCount),
    reportType: inferReportType(wordCount),
    shelf,
    genre: genre ?? null,
  };
}

function appendDreamTxtSections(lines: string[], dream: LongformDreamDocument): void {
  const sep = '='.repeat(72);
  const sub = '-'.repeat(72);
  const push = (s: string) => lines.push(s);

  push('');
  push(sep);
  push('NARRATIVE SYNTHESIS—HOLISTIC CRAFT ASSESSMENT');
  push(sep);
  push('');
  push(`Quality: ${dream.dream_scores?.quality ?? '—'}/100`);
  push(`Readiness: ${dream.dream_scores?.readiness ?? '—'}/100`);
  push(`Commercial: ${dream.dream_scores?.commercial ?? '—'}/100`);
  push(`Literary: ${dream.dream_scores?.literary ?? '—'}/100`);
  push('');
  push('Executive Verdict:');
  push(cleanReportText(dream.executive_verdict));

  if (dream.market_shelf) {
    push('');
    push(sub);
    push('MARKET SHELF');
    push(sub);
    push(`Best shelf: ${cleanReportText(dream.market_shelf.best_shelf)}`);
    push(`Marketable hook: ${cleanReportText(dream.market_shelf.marketable_hook)}`);
    if (Array.isArray(dream.market_shelf.shelf_neighbors) && dream.market_shelf.shelf_neighbors.length > 0) {
      push('Shelf neighbors:');
      dream.market_shelf.shelf_neighbors.forEach((item) => push(`  • ${cleanReportText(item)}`));
    }
    if (Array.isArray(dream.market_shelf.comparison_space) && dream.market_shelf.comparison_space.length > 0) {
      push('Comparison space:');
      dream.market_shelf.comparison_space.forEach((item) => push(`  • ${cleanReportText(item)}`));
    }
    push(`Market danger: ${cleanReportText(dream.market_shelf.market_danger)}`);
  }

  if (Array.isArray(dream.structural_stack) && dream.structural_stack.length > 0) {
    push('');
    push(sub);
    push('STRUCTURAL STACK');
    push(sub);
    dream.structural_stack.forEach((layer) => {
      push('');
      push(`${cleanReportText(layer.layer_name)}—${layer.status}`);
      push(`  Function: ${cleanReportText(layer.function)}`);
      push(`  Revision note: ${cleanReportText(layer.revision_note)}`);
    });
  }

  if (Array.isArray(dream.arc_map) && dream.arc_map.length > 0) {
    push('');
    push(sub);
    push('ARC MAP');
    push(sub);
    dream.arc_map.forEach((act) => {
      push('');
      push(`${cleanReportText(act.act_name)} (${cleanReportText(act.chapter_range)})`);
      push(`  Function: ${cleanReportText(act.primary_function)}`);
      push(`  Revision priority: ${cleanReportText(act.revision_priority)}`);
    });
  }

  if (Array.isArray(dream.criterion_analyses) && dream.criterion_analyses.length > 0) {
    push('');
    push(sub);
    push('EXPANDED CRITERION ANALYSIS');
    push(sub);
    dream.criterion_analyses.forEach((a) => {
      push('');
      push(`${a.key}—${a.score}/10 (${formatConfidenceLabel(a.confidence)})`);
      pushTxtListBlock(lines, 'What is working', a.fit_evidence);
      pushTxtListBlock(lines, 'What weakens impact', a.gap_evidence);
      pushTxtListBlock(lines, 'Revision queue', a.revision_queue, { ordered: true });
    });
  }

  const revisionPlan = getRenumberedAuthorFacingRevisionPlan(dream.revision_plan);
  if (revisionPlan.length > 0) {
    push('');
    push(sub);
    push('REVISION PLAN');
    push(sub);
    revisionPlan.forEach((item) => {
      push('');
      push(`Priority ${item.displayPriority}: ${cleanReportText(item.title)}`);
      push(`  Goal: ${cleanReportText(item.goal)}`);
      pushTxtListBlock(lines, 'Actions', item.actions, { ordered: true });
      if (item.acceptance_check) push(`  Acceptance check: ${cleanReportText(item.acceptance_check)}`);
    });
  }
}

function buildTxtReport(result: ExportableResult, title: string | null, jobId: string, dream: LongformDreamDocument | null, waveGov: WaveGovernanceData | null = null, gate15?: Gate15AuditArtifact | null, goldenSpine?: GoldenSpineArtifact | null, dialogueCanon?: DialogueCanonAuditArtifact | null, revisionCanonMeta?: RevisionCanonMetadata | null): string {
  const lines: string[] = [];
  const sep = '='.repeat(72);
  const sub = '-'.repeat(72);
  const metadata = buildMetadata(result, title, dream);
  const summaryFallback = buildSummaryFallback(result);

  lines.push(sep);
  lines.push('REVISIONGRADE™ EVALUATION REPORT');
  lines.push(sep);
  lines.push(`Manuscript Title: ${metadata.displayTitle}`);
  lines.push(`Report Type: ${metadata.reportType}`);
  if (metadata.genre) lines.push(`Genre: ${metadata.genre}`);
  if (metadata.shelf) lines.push(`Shelf: ${metadata.shelf}`);
  if (metadata.wordCount) lines.push(`Submitted Word Count: ${metadata.wordCount.toLocaleString()}`);
  if (metadata.estimatedPages) lines.push(`Estimated Manuscript Pages: ${metadata.estimatedPages.toLocaleString()} at ${WORDS_PER_MANUSCRIPT_PAGE} words/page`);
  lines.push(`Generated: ${metadata.generatedAt}`);
  lines.push(`Overall Score: ${metadata.score}`);
  lines.push(`Verdict: ${metadata.verdict}`);
  lines.push('Confidentiality: Prepared for author/editorial use.');
  lines.push('');

  lines.push(sub);
  lines.push('SUMMARY');
  lines.push(sub);
  lines.push(cleanReportText(result.overview.one_paragraph_summary, summaryFallback, { blockTruncation: true }));
  lines.push('');

  lines.push(sub);
  lines.push('TOP STRENGTHS');
  lines.push(sub);
  result.overview.top_3_strengths.forEach((s, i) => lines.push(`${i + 1}. ${cleanReportText(s)}`));
  lines.push('');

  lines.push(sub);
  lines.push('TOP RISKS');
  lines.push(sub);
  result.overview.top_3_risks.forEach((r, i) => lines.push(`${i + 1}. ${cleanReportText(r)}`));
  lines.push('');

  lines.push(sub);
  lines.push('CRITERIA SCORES');
  lines.push(sub);
  result.criteria.forEach((c) => {
    const criterionRecord = c as Record<string, unknown>;
    lines.push(`• ${getCriterionDisplayLabel(c.key)}—${scoreLabel(c.score_0_10, 10)}${c.confidence_level ? ` (${formatConfidenceLabel(c.confidence_level)})` : ''}`);
    if (c.rationale) lines.push(`  Rationale: ${cleanReportText(c.rationale)}`);

    if (typeof criterionRecord.fit_summary === 'string' && criterionRecord.fit_summary.trim()) {
      lines.push(`  What's Working: ${cleanReportText(criterionRecord.fit_summary)}`);
    }
    if (typeof criterionRecord.gap_summary === 'string' && criterionRecord.gap_summary.trim()) {
      lines.push(`  Gap to Close: ${cleanReportText(criterionRecord.gap_summary)}`);
    }

    const opportunities = getCriterionOpportunities(c as { recommendations?: unknown });
    if (opportunities.length > 0) {
      lines.push('  Opportunities:');
      opportunities.forEach((opportunity, idx) => {
        lines.push(`    ${idx + 1}. [${exportSeverity(opportunity.priority)}]`);
        opportunityRows(opportunity).forEach(([label, value]) => {
          lines.push(`       ${label}: ${value}`);
        });
      });
    }

    lines.push('');
  });

  if (dream) appendDreamTxtSections(lines, dream);

  if (waveGov) {
    lines.push('');
    lines.push(sub);
    lines.push('WAVE GOVERNANCE & CANON EXECUTION');
    lines.push(sub);
    const waveStatusLabel = waveGov.planStatus === 'complete' ? 'EXECUTED'
      : waveGov.planStatus === 'skipped' ? 'SKIPPED'
      : waveGov.planStatus === 'failed' ? 'FAILED'
      : waveGov.planStatus === 'timeout' ? 'TIMEOUT' : 'UNKNOWN';
    lines.push(`WAVE Engine Status: ${waveStatusLabel}`);
    lines.push(`Modules Run: ${waveGov.modulesRun}`);
    lines.push(`Modules With Findings: ${waveGov.modulesWithFindings}`);
    if (waveGov.planStatus === 'skipped' && waveGov.reasonCodes.length > 0) {
      lines.push('Gate Failure Reasons:');
      waveGov.reasonCodes.forEach((code) => lines.push(`  - ${code}`));
    }
    if (waveGov.wavePlanSummary) {
      lines.push(`Derived Waves: ${waveGov.wavePlanSummary.derivedWaveIds.length}`);
      lines.push(`Plan Valid: ${waveGov.wavePlanSummary.planValid ? 'Yes' : 'No'}`);
    }
    if (waveGov.waveRuns.length > 0) {
      lines.push('');
      lines.push('Wave Module Execution:');
      waveGov.waveRuns.forEach((run) => {
        lines.push(`  W-${String(run.waveNumber).padStart(2, '0')} | ${run.waveName} | ${run.category} | ${run.status} | ${run.changesCount} changes | ${run.durationMs > 0 ? (run.durationMs / 1000).toFixed(1) + 's' : '\u2014'}`);
      });
    }
    if (waveGov.generatedAt) {
      lines.push(`WAVE report generated: ${new Date(waveGov.generatedAt).toISOString()}`);
    }
  }

  // Canon Governance: Gate 15
  if (gate15) {
    lines.push('');
    lines.push(sub);
    lines.push('GATE 15 — PAIRED GATE AUDIT');
    lines.push(sub);
    lines.push(`Overall Status: ${gate15.overallStatus}`);
    lines.push(`Word Count: ${gate15.wordCount.toLocaleString()}`);
    if (gate15.activatedBecause) lines.push(`Activated: ${gate15.activatedBecause}`);
    if (gate15.skippedBecause) lines.push(`Skipped: ${gate15.skippedBecause}`);
    if (gate15.gate15_1?.layer1) {
      lines.push('');
      lines.push('Gate 15.1 — Structural Purity:');
      const l1 = gate15.gate15_1.layer1;
      lines.push(`  Q1 Attribution Density: ${l1.attributionDensity.normalized}/1000 (threshold: ≤${l1.attributionDensity.threshold}/1000) → ${l1.attributionDensity.status}`);
      lines.push(`  Q2 Soft-Tag Cap: ${l1.softTags.normalized}/ch (threshold: ≤${l1.softTags.threshold}/ch) → ${l1.softTags.status}`);
      lines.push(`  Q3 Thought-Verb: ${l1.thoughtVerbs.normalized}/ch (threshold: ≤${l1.thoughtVerbs.threshold}/ch) → ${l1.thoughtVerbs.status}`);
      lines.push(`  Q4 Physiological Filler: ${l1.physiologicalFillers.normalized}/ch (threshold: ≤${l1.physiologicalFillers.threshold}/ch) → ${l1.physiologicalFillers.status}`);
      lines.push(`  Q5 Boundary Test: ${l1.boundaryTest.unmatchedQuotes} unmatched quotes, ${l1.boundaryTest.unmatchedItalics} unmatched italics → ${l1.boundaryTest.status}`);
    }
    if (gate15.gate15_2 && !gate15.gate15_2.skippedBecause) {
      lines.push('');
      lines.push('Gate 15.2 — Voice & Meaning Protection:');
      lines.push(`  Protected: ${gate15.gate15_2.protectedSegments}, Trim: ${gate15.gate15_2.trimSegments}, Cut: ${gate15.gate15_2.cutSegments}`);
      lines.push(`  Overcorrection Risk: ${gate15.gate15_2.overcorrectionRiskLevel}`);
    }
    gate15.summaryFindings.forEach(f => lines.push(`  • ${f}`));
  }

  // Canon Governance: Golden Spine
  if (goldenSpine && goldenSpine.overallStatus !== 'skipped') {
    lines.push('');
    lines.push(sub);
    lines.push('GOLDEN SPINE — MOTIF LEDGER');
    lines.push(sub);
    lines.push(`Continuity Score: ${goldenSpine.continuityScore}`);
    lines.push(`Motifs Tracked: ${goldenSpine.motifLedger.length}`);
    if (goldenSpine.spines.length > 0) {
      lines.push('Narrative Spines:');
      goldenSpine.spines.forEach(s => lines.push(`  ${s.type}: ${s.label}`));
    }
    goldenSpine.motifLedger.slice(0, 15).forEach(m => {
      lines.push(`  ${m.motif} (${m.category}) | ${m.occurrences}x | ${m.firstAppearance}→${m.lastAppearance} | payoff: ${m.payoffStatus} | revision: ${m.revisionNeed}`);
    });
    goldenSpine.summaryFindings.forEach(f => lines.push(`  • ${f}`));
  }

  // Canon Governance: Dialogue Canon
  if (dialogueCanon && dialogueCanon.overallStatus !== 'skipped') {
    lines.push('');
    lines.push(sub);
    lines.push('DIALOGUE CANON AUDIT');
    lines.push(sub);
    lines.push(`Dialogue Status: ${dialogueCanon.dialogueStatus}`);
    lines.push(`Lines: ${dialogueCanon.metrics.totalDialogueLines}, Speakers: ${dialogueCanon.metrics.uniqueSpeakers}, Attributed: ${dialogueCanon.metrics.attributedLines}`);
    lines.push(`Exposition Leakage Rate: ${(dialogueCanon.metrics.expositionLeakageRate * 100).toFixed(1)}%`);
    dialogueCanon.summaryFindings.forEach(f => lines.push(`  • ${f}`));
  }

  // Canon Governance: Revision Canon Metadata
  if (revisionCanonMeta && revisionCanonMeta.overallStatus !== 'skipped') {
    lines.push('');
    lines.push(sub);
    lines.push('REVISION CANON METADATA');
    lines.push(sub);
    lines.push(`Criteria Enriched: ${revisionCanonMeta.attributions.length}`);
    const categories = revisionCanonMeta.attributions.reduce<Record<string, number>>((acc, a) => {
      acc[a.canonRiskCategory] = (acc[a.canonRiskCategory] || 0) + 1;
      return acc;
    }, {});
    Object.entries(categories).forEach(([cat, count]) => {
      lines.push(`  ${cat.replace(/_/g, ' ')}: ${count} criteria`);
    });
    revisionCanonMeta.summaryFindings.forEach(f => lines.push(`  • ${f}`));
  }

  lines.push('');
  lines.push(sep);
  lines.push(EXPORT_DISCLAIMER);
  return lines.join('\n');
}

function verdictColors(verdict: string): { bg: string; text: string } {
  const v = verdict.toLowerCase();
  if (v === 'pass') return { bg: RG.successBg, text: RG.success };
  if (v === 'revise') return { bg: RG.warningBg, text: RG.warning };
  return { bg: RG.errorBg, text: RG.error };
}

function confidenceColor(level: string | undefined): string {
  if (level === 'high') return RG.success;
  if (level === 'moderate') return RG.warning;
  if (level === 'low') return RG.error;
  return RG.textMuted;
}

function formatConfidenceLabel(level: string | undefined): string {
  if (!level) return '—';
  const normalized = level.trim().toLowerCase();
  if (normalized === 'high') return 'High Confidence';
  if (normalized === 'moderate') return 'Moderate Confidence';
  if (normalized === 'low') return 'Low Confidence';
  return `${level.charAt(0).toUpperCase()}${level.slice(1)} Confidence`;
}

function scoreBarColor(score: number | null | undefined): string {
  if (typeof score !== 'number') return RG.textFaint;
  if (score >= 8) return RG.success;
  if (score >= 6) return RG.warning;
  return RG.error;
}

async function buildPdfReport(result: ExportableResult, title: string | null, jobId: string, dream: LongformDreamDocument | null): Promise<Buffer> {
  return await new Promise<Buffer>((resolve, reject) => {
    const metadata = buildMetadata(result, title, dream);
    const summaryFallback = buildSummaryFallback(result);
    const doc = new PDFDocument({ size: 'LETTER', margin: 54, bufferPages: true });
    const chunks: Buffer[] = [];
    doc.on('data', (chunk: Buffer | Uint8Array) => chunks.push(Buffer.isBuffer(chunk) ? chunk : Buffer.from(chunk)));
    doc.on('end', () => resolve(Buffer.concat(chunks)));
    doc.on('error', reject);

    const ml = doc.page.margins.left;
    const contentWidth = doc.page.width - ml - doc.page.margins.right;
    const bottomY = () => doc.page.height - doc.page.margins.bottom - 48;
    const ensureSpace = (height = 90) => {
      if (doc.y + height > bottomY()) doc.addPage();
    };

    // ── Branded helpers ───────────────────────────────────────────────
    const goldRule = () => {
      doc.moveTo(ml, doc.y).lineTo(ml + contentWidth, doc.y).strokeColor(RG.gold).lineWidth(1.5).stroke();
      doc.strokeColor('#000000').lineWidth(1);
      doc.moveDown(0.4);
    };
    const thinRule = () => {
      doc.moveTo(ml, doc.y).lineTo(ml + contentWidth, doc.y).strokeColor(RG.border).lineWidth(0.5).stroke();
      doc.strokeColor('#000000').lineWidth(1);
      doc.moveDown(0.3);
    };
    const section = (text: unknown) => {
      ensureSpace(72);
      doc.moveDown(0.8);
      doc.font('Helvetica-Bold').fontSize(14).fillColor(RG.oxblood).text(toPdfSafeText(text), ml, doc.y, { width: contentWidth });
      doc.moveDown(0.25);
      goldRule();
    };
    const paragraph = (text: unknown) => {
      ensureSpace(70);
      doc.font('Helvetica').fontSize(10.5).fillColor(RG.textPrimary).text(toPdfSafeText(text), ml, doc.y, { width: contentWidth, lineGap: 3 });
      doc.moveDown(0.45);
    };
    const bullet = (text: unknown, color: string = RG.textPrimary) => {
      ensureSpace(45);
      doc.font('Helvetica').fontSize(10.5).fillColor(color).text(`  -  ${toPdfSafeText(text)}`, ml, doc.y, { width: contentWidth, indent: 8, lineGap: 2 });
      doc.moveDown(0.25);
    };
    const labelValue = (label: string, value: unknown) => {
      const safeValue = toPdfSafeText(value, '');
      if (!safeValue) return;
      const labelStr = `${toPdfSafeText(label)}:  `;
      doc.font('Helvetica-Bold').fontSize(10);
      const labelWidth = doc.widthOfString(labelStr);
      doc.fillColor(RG.textMuted).text(labelStr, ml, doc.y, { continued: true });
      doc.font('Helvetica').fillColor(RG.textPrimary).text(safeValue, { width: contentWidth - labelWidth });
      doc.moveDown(0.2);
    };

    // ── Cover page ────────────────────────────────────────────────────
    // Top gold accent bar
    doc.rect(0, 0, doc.page.width, 6).fill(RG.gold);

    doc.moveDown(2.5);
    doc.font('Helvetica-Bold').fontSize(28).fillColor(RG.oxblood).text(
      toPdfSafeText('RevisionGrade(TM)'),
      { width: contentWidth, align: 'center' },
    );
    doc.moveDown(0.15);
    doc.font('Helvetica').fontSize(10).fillColor(RG.textMuted).text(
      'Manuscript diagnosis, author-controlled revision, and professional submission preparation.',
      { width: contentWidth, align: 'center' },
    );
    doc.moveDown(1.5);
    goldRule();
    doc.moveDown(0.5);

    // Title
    doc.font('Helvetica-Bold').fontSize(20).fillColor(RG.textPrimary).text(
      toPdfSafeText(metadata.displayTitle),
      { width: contentWidth, align: 'center' },
    );
    doc.moveDown(0.3);
    doc.font('Helvetica').fontSize(11).fillColor(RG.textMuted).text(
      metadata.reportType,
      { width: contentWidth, align: 'center' },
    );
    doc.moveDown(1.5);

    // Score + Verdict block
    const vColors = verdictColors(metadata.verdict);
    const scoreNum = result.overview.overall_score_0_100;
    // Score circle area
    const circleX = ml + contentWidth / 2;
    const circleY = doc.y + 36;
    doc.circle(circleX, circleY, 34).fillAndStroke(RG.surface, RG.gold);
    doc.font('Helvetica-Bold').fontSize(26).fillColor(RG.textPrimary).text(
      `${Math.round(scoreNum)}`,
      circleX - 30, circleY - 16,
      { width: 60, align: 'center' },
    );
    doc.font('Helvetica').fontSize(8).fillColor(RG.textMuted).text(
      'out of 100',
      circleX - 30, circleY + 12,
      { width: 60, align: 'center' },
    );
    doc.y = circleY + 46;

    // Verdict badge
    doc.moveDown(0.5);
    const badgeWidth = 100;
    const badgeX = circleX - badgeWidth / 2;
    doc.roundedRect(badgeX, doc.y, badgeWidth, 22, 4).fill(vColors.bg);
    doc.font('Helvetica-Bold').fontSize(11).fillColor(vColors.text).text(
      metadata.verdict,
      badgeX, doc.y + 5,
      { width: badgeWidth, align: 'center' },
    );
    doc.y += 32;
    doc.moveDown(1.2);

    // Metadata block
    thinRule();
    doc.moveDown(0.3);
    labelValue('Genre', metadata.genre);
    labelValue('Shelf', metadata.shelf);
    labelValue('Submitted Word Count', metadata.wordCount ? metadata.wordCount.toLocaleString() : null);
    labelValue('Estimated Manuscript Pages', metadata.estimatedPages ? `${metadata.estimatedPages.toLocaleString()} at ${WORDS_PER_MANUSCRIPT_PAGE} words/page` : null);
    labelValue('Generated', metadata.generatedAt);
    labelValue('Confidentiality', 'Prepared for author/editorial use');
    doc.moveDown(1.5);

    // Bottom gold accent bar on cover
    thinRule();
    doc.moveDown(0.3);
    doc.font('Helvetica').fontSize(8.5).fillColor(RG.textFaint).text(
      toPdfSafeText(EXPORT_DISCLAIMER),
      { width: contentWidth, align: 'center' },
    );

    // ── Page 2+: Report content ───────────────────────────────────────
    doc.addPage();

    section('Executive Summary');
    paragraph(cleanReportText(result.overview.one_paragraph_summary, summaryFallback, { blockTruncation: true }));

    // Strengths & Risks side by side concept (rendered sequentially for pdfkit)
    section('Top Strengths');
    if (result.overview.top_3_strengths.length > 0) {
      result.overview.top_3_strengths.forEach((s) => bullet(s, RG.success));
    } else {
      paragraph('(none)');
    }

    section('Top Risks');
    if (result.overview.top_3_risks.length > 0) {
      result.overview.top_3_risks.forEach((r) => bullet(r, RG.warning));
    } else {
      paragraph('(none)');
    }

    // ── Criteria Scores with visual bars ──────────────────────────────
    section('Criteria Scores');
    result.criteria.forEach((c) => {
      ensureSpace(100);
      const score = c.score_0_10;
      const barColor = scoreBarColor(score);
      const confColor = confidenceColor(c.confidence_level);

      // Criterion name + score
      doc.font('Helvetica-Bold').fontSize(11).fillColor(RG.textPrimary).text(
        toPdfSafeText(getCriterionDisplayLabel(c.key)),
        ml, doc.y,
        { width: contentWidth - 80, continued: false },
      );
      // Score on the right
      const scoreY = doc.y - 14;
      doc.font('Helvetica-Bold').fontSize(13).fillColor(barColor).text(
        scoreLabel(score, 10),
        ml + contentWidth - 70, scoreY,
        { width: 70, align: 'right' },
      );
      doc.y = Math.max(doc.y, scoreY + 16);

      // Score bar
      const barY = doc.y;
      const barWidth = contentWidth - 90;
      const barHeight = 6;
      // Background bar
      doc.roundedRect(ml, barY, barWidth, barHeight, 3).fill(RG.border);
      // Filled bar
      if (typeof score === 'number' && score > 0) {
        const fillWidth = Math.max(6, (score / 10) * barWidth);
        doc.roundedRect(ml, barY, fillWidth, barHeight, 3).fill(barColor);
      }
      // Confidence label
      if (c.confidence_level) {
        doc.font('Helvetica').fontSize(8).fillColor(confColor).text(
          formatConfidenceLabel(c.confidence_level),
          ml + barWidth + 8, barY - 1,
          { width: 80 },
        );
      }
      doc.y = barY + barHeight + 6;

      // Rationale
      if (c.rationale) {
        doc.font('Helvetica').fontSize(9.5).fillColor(RG.textMuted).text(
          toPdfSafeText(c.rationale),
          ml + 4, doc.y,
          { width: contentWidth - 8, lineGap: 2 },
        );
      }

      const opportunities = getCriterionOpportunities(c as { recommendations?: unknown });
      if (opportunities.length > 0) {
        doc.moveDown(0.3);
        doc.font('Helvetica-Bold').fontSize(9.5).fillColor(RG.oxblood).text('Opportunities', ml + 4, doc.y, {
          width: contentWidth - 8,
        });
        doc.moveDown(0.15);

        opportunities.forEach((opportunity) => {
          ensureSpace(90);
          doc.font('Helvetica-Bold').fontSize(9).fillColor(RG.textPrimary).text(
            `[${exportSeverity(opportunity.priority)}]`,
            ml + 10,
            doc.y,
            { width: contentWidth - 20 },
          );

          const rows = opportunityRows(opportunity);
          const evidence = rows.find(([label]) => label === 'Evidence');
          const detailRows = rows.filter(([label]) => label !== 'Evidence');

          if (evidence) {
            doc.font('Helvetica-Oblique').fontSize(8.5).fillColor(RG.textMuted).text(
              `Evidence: "${toPdfSafeText(evidence[1])}"`,
              ml + 14,
              doc.y + 1,
              { width: contentWidth - 24, lineGap: 2 },
            );
          }

          detailRows.forEach(([label, value]) => {
            doc.font('Helvetica-Bold').fontSize(8.5).fillColor(RG.textMuted).text(
              `${label}: `,
              ml + 14,
              doc.y + 1,
              { continued: true },
            );
            doc.font('Helvetica').fontSize(8.5).fillColor(RG.textPrimary).text(toPdfSafeText(value), {
              width: contentWidth - 24,
              lineGap: 2,
            });
          });

          doc.moveDown(0.2);
        });
      }

      doc.moveDown(0.6);
      thinRule();
    });

    // ── Dream / Narrative Synthesis (longform) ────────────────────────
    if (dream) {
      section('Narrative Synthesis');
      // Dream scores row
      ensureSpace(60);
      const dreamLabels = ['Quality', 'Readiness', 'Commercial', 'Literary'];
      const dreamScores = [
        dream.dream_scores?.quality,
        dream.dream_scores?.readiness,
        dream.dream_scores?.commercial,
        dream.dream_scores?.literary,
      ];
      const colW = contentWidth / 4;
      const rowY = doc.y;
      dreamLabels.forEach((label, i) => {
        const x = ml + i * colW;
        const val = dreamScores[i];
        doc.font('Helvetica').fontSize(8).fillColor(RG.textMuted).text(label, x, rowY, { width: colW, align: 'center' });
        doc.font('Helvetica-Bold').fontSize(16).fillColor(RG.textPrimary).text(
          `${val ?? '-'}`,
          x, rowY + 12,
          { width: colW, align: 'center' },
        );
        doc.font('Helvetica').fontSize(7).fillColor(RG.textFaint).text('/100', x, rowY + 30, { width: colW, align: 'center' });
      });
      doc.x = ml;
      doc.y = rowY + 44;
      doc.moveDown(0.5);
      thinRule();

      doc.font('Helvetica-Bold').fontSize(12).fillColor(RG.oxblood).text('Executive Verdict', ml, doc.y, { width: contentWidth });
      doc.moveDown(0.2);
      paragraph(dream.executive_verdict);

      if (dream.market_shelf) {
        section('Market Shelf');
        labelValue('Best Shelf', dream.market_shelf.best_shelf);
        labelValue('Marketable Hook', dream.market_shelf.marketable_hook);
        labelValue('Market Danger', dream.market_shelf.market_danger);
      }

      if (Array.isArray(dream.structural_stack) && dream.structural_stack.length > 0) {
        section('Structural Stack');
        dream.structural_stack.forEach((layer) => {
          ensureSpace(90);
          doc.font('Helvetica-Bold').fontSize(10.5).fillColor(RG.textPrimary).text(
            toPdfSafeText(`${cleanReportText(layer.layer_name)} -- ${layer.status}`),
            ml, doc.y,
            { width: contentWidth },
          );
          paragraph(`Function: ${cleanReportText(layer.function)}`);
          paragraph(`Revision note: ${cleanReportText(layer.revision_note)}`);
        });
      }

      if (Array.isArray(dream.arc_map) && dream.arc_map.length > 0) {
        section('Arc Map');
        dream.arc_map.forEach((act) => {
          ensureSpace(80);
          doc.font('Helvetica-Bold').fontSize(10.5).fillColor(RG.textPrimary).text(
            toPdfSafeText(`${cleanReportText(act.act_name)} (${cleanReportText(act.chapter_range)})`),
            { width: contentWidth },
          );
          paragraph(`Function: ${cleanReportText(act.primary_function)}`);
          paragraph(`Revision priority: ${cleanReportText(act.revision_priority)}`);
        });
      }

      const revisionPlan = getRenumberedAuthorFacingRevisionPlan(dream.revision_plan);
      if (revisionPlan.length > 0) {
        section('Revision Plan');
        revisionPlan.forEach((item) => {
          ensureSpace(90);
          doc.font('Helvetica-Bold').fontSize(10.5).fillColor(RG.textPrimary).text(
            toPdfSafeText(`Priority ${item.displayPriority}: ${cleanReportText(item.title)}`),
            { width: contentWidth },
          );
          paragraph(`Goal: ${cleanReportText(item.goal)}`);
          if (Array.isArray(item.actions) && item.actions.length > 0) item.actions.forEach((a) => bullet(a));
          if (item.acceptance_check) paragraph(`Acceptance check: ${cleanReportText(item.acceptance_check)}`);
        });
      }

      // Expanded Criterion Analysis from dream document
      if (Array.isArray(dream.criterion_analyses) && dream.criterion_analyses.length > 0) {
        section('Expanded Criterion Analysis');
        dream.criterion_analyses.forEach((a) => {
          ensureSpace(120);
          const confLabel = formatConfidenceLabel(a.confidence);
          doc.font('Helvetica-Bold').fontSize(11).fillColor(RG.textPrimary).text(
            toPdfSafeText(getCriterionDisplayLabel(a.key)),
            ml, doc.y,
            { width: contentWidth - 120, continued: true },
          );
          doc.font('Helvetica-Bold').fontSize(11).fillColor(scoreBarColor(a.score)).text(
            toPdfSafeText(` -- ${a.score}/10`),
            { continued: true },
          );
          doc.font('Helvetica').fontSize(9.5).fillColor(confidenceColor(a.confidence)).text(
            ` (${toPdfSafeText(confLabel)})`,
          );
          doc.moveDown(0.25);

          const fitItems = filterAuthorFacingTextList(a.fit_evidence);
          if (fitItems.length > 0) {
            doc.font('Helvetica-Bold').fontSize(9.5).fillColor(RG.success).text('What Is Working:', ml, doc.y, { width: contentWidth });
            doc.moveDown(0.1);
            fitItems.forEach((item) => bullet(item, RG.success));
          }

          const gapItems = filterAuthorFacingTextList(a.gap_evidence);
          if (gapItems.length > 0) {
            doc.font('Helvetica-Bold').fontSize(9.5).fillColor(RG.warning).text('What Weakens Impact:', ml, doc.y, { width: contentWidth });
            doc.moveDown(0.1);
            gapItems.forEach((item) => bullet(item, RG.warning));
          }

          const revItems = filterAuthorFacingTextList(a.revision_queue);
          if (revItems.length > 0) {
            doc.font('Helvetica-Bold').fontSize(9.5).fillColor(RG.oxblood).text('Revision Queue:', ml, doc.y, { width: contentWidth });
            doc.moveDown(0.1);
            revItems.forEach((item, idx) => {
              ensureSpace(35);
              doc.font('Helvetica').fontSize(10).fillColor(RG.textPrimary).text(
                `  ${idx + 1}. ${toPdfSafeText(item)}`,
                ml, doc.y,
                { width: contentWidth, indent: 8, lineGap: 2 },
              );
              doc.moveDown(0.15);
            });
          }

          doc.moveDown(0.4);
          thinRule();
        });
      }
    }

    // ── Branded footers on every page ─────────────────────────────────
    const range = doc.bufferedPageRange();
    for (let i = range.start; i < range.start + range.count; i += 1) {
      doc.switchToPage(i);
      const pageNumber = i - range.start + 1;
      const footerY = doc.page.height - 40;

      // Gold line above footer
      doc.moveTo(ml, footerY - 8).lineTo(ml + contentWidth, footerY - 8)
        .strokeColor(RG.gold).lineWidth(0.5).stroke();
      doc.strokeColor('#000000').lineWidth(1);

      if (pageNumber === 1) {
        doc.font('Helvetica').fontSize(7).fillColor(RG.textFaint).text(
          toPdfSafeText(FOOTER_LINE),
          ml, footerY,
          { width: contentWidth, align: 'center' },
        );
      } else {
        doc.font('Helvetica').fontSize(7).fillColor(RG.textFaint).text(
          toPdfSafeText(`RevisionGrade(TM) Evaluation Report  --  Confidential  --  Page ${pageNumber}`),
          ml, footerY,
          { width: contentWidth, align: 'center' },
        );
      }
      doc.fillColor('#000000');
    }

    doc.end();
  });
}

